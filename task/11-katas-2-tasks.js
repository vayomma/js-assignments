/**
 * Returns the string, but with line breaks inserted at just the right places to make sure that no line is longer than the specified column number.
 * Lines can be broken at word boundaries only.
 *
 * @param {string} text
 * @param {number} columns
 * @return {Iterable.<string>}
 *
 * @example :
 *
 *  'The String global object is a constructor for strings, or a sequence of characters.', 26 =>  'The String global object',
 *                                                                                                'is a constructor for',
 *                                                                                                'strings, or a sequence of',
 *                                                                                                'characters.'
 *
 *  'The String global object is a constructor for strings, or a sequence of characters.', 12 =>  'The String',
 *                                                                                                'global',
 *                                                                                                'object is a',
 *                                                                                                'constructor',
 *                                                                                                'for strings,',
 *                                                                                                'or a',
 *                                                                                                'sequence of',
 *                                                                                                'characters.'
 */
function* wrapText(text, columns) {
    let idx, tail = text;
    while (tail.length > columns) {
        idx = tail.slice(0, columns + 1).lastIndexOf(' ');
        yield tail.slice(0, idx).trim();
        tail = tail.slice(idx + 1).trim();
    }
    yield tail;
    // throw new Error('Not implemented');
}


/**
 * Returns the rank of the specified poker hand.
 * See the ranking rules here: https://en.wikipedia.org/wiki/List_of_poker_hands.
 *
 * @param {array} hand
 * @return {PokerRank} rank
 *
 * @example
 *   [ '4♥','5♥','6♥','7♥','8♥' ] => PokerRank.StraightFlush
 *   [ 'A♠','4♠','3♠','5♠','2♠' ] => PokerRank.StraightFlush
 *   [ '4♣','4♦','4♥','4♠','10♥' ] => PokerRank.FourOfKind
 *   [ '4♣','4♦','5♦','5♠','5♥' ] => PokerRank.FullHouse
 *   [ '4♣','5♣','6♣','7♣','Q♣' ] => PokerRank.Flush
 *   [ '2♠','3♥','4♥','5♥','6♥' ] => PokerRank.Straight
 *   [ '2♥','4♦','5♥','A♦','3♠' ] => PokerRank.Straight
 *   [ '2♥','2♠','2♦','7♥','A♥' ] => PokerRank.ThreeOfKind
 *   [ '2♥','4♦','4♥','A♦','A♠' ] => PokerRank.TwoPairs
 *   [ '3♥','4♥','10♥','3♦','A♠' ] => PokerRank.OnePair
 *   [ 'A♥','K♥','Q♥','2♦','3♠' ] =>  PokerRank.HighCard
 */
const PokerRank = {
    StraightFlush: 8,
    FourOfKind: 7,
    FullHouse: 6,
    Flush: 5,
    Straight: 4,
    ThreeOfKind: 3,
    TwoPairs: 2,
    OnePair: 1,
    HighCard: 0
}

function getPokerHandRank(hand) {
    let positions = new Array(14).fill(0);
    let ranks = hand.map(card => card.slice(0, -1)).map(rank => {
        return (rank === 'A' && 1)
            || (rank === 'J' && 11) 
            || (rank === 'Q' && 12)
            || (rank === 'K' && 13)
            || Number(rank);
    });
    let flush = hand.every(value => value.slice(-1) === hand[0].slice(-1));
    
    ranks.forEach(rank => {
        positions[rank - 1] += 1;
        if (rank === 1) positions[13] += 1;
    });
    let straight = positions.join('').indexOf('11111') != -1;
    
    if (flush && straight) return PokerRank.StraightFlush;
    if (positions.indexOf(4) >= 0) return PokerRank.FourOfKind;
    if (positions.indexOf(3) >= 0 && positions.indexOf(2) >= 0) return PokerRank.FullHouse;
    if (flush) return PokerRank.Flush;
    if (straight) return PokerRank.Straight;
    if (positions.indexOf(3) >= 0) return PokerRank.ThreeOfKind;
    if (positions.slice(1).join('').match(/22|2[01]+2/)) return PokerRank.TwoPairs;
    if (positions.indexOf(2) >= 0) return PokerRank.OnePair;
    return PokerRank.HighCard;
    // throw new Error('Not implemented');
}


